package com.hundsun.cprs.advertisement.request;

public class AdPositionRequest {
	private String adPositioncode;//广告位编码
	private String adPositionTypeCode;//广告位分类编码
	public String getAdPositioncode() {
		return adPositioncode;
	}
	public void setAdPositioncode(String adPositioncode) {
		this.adPositioncode = adPositioncode;
	}
	public String getAdPositionTypeCode() {
		return adPositionTypeCode;
	}
	public void setAdPositionTypeCode(String adPositionTypeCode) {
		this.adPositionTypeCode = adPositionTypeCode;
	}
	
}
